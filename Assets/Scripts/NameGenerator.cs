    public static string GenerateBandName()
    {
        string name = "";
        if (Random.Range(0, 2) == 0)
            name += "The ";
        if (Random.Range(0, 2) == 0)
            name += FirstCharToUpper(adjectives[Random.Range(0, adjectives.Length)]) + " ";
        string noun = FirstCharToUpper(singularNouns[Random.Range(0, singularNouns.Length)]);
        if (Random.Range(0, 2) == 0)
            name += noun;
        else
            name += Pluralizer.Pluralize(noun);
        return name;
    }

    public static string GeneratePersonName() 
    {
        string name = "";
        if (Random.Range(0, 3) != 0)
            name += FirstCharToUpper(personAdjectives[Random.Range(0, personAdjectives.Length)]) + " ";
        name += firstNames[Random.Range(0, firstNames.Length)];
        if (Random.Range(0, 3) == 0)
            // add random capital character
            name += " " + alphabet[Random.Range(0, alphabet.Length)].ToString().ToUpper() + '.';
        return name;
    }

    private static string FirstCharToUpper(string input)
    {
        return input[0].ToString().ToUpper() + input.Substring(1);
    }

